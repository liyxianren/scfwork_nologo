function attachSchedule(project) {
  const sessions = PROJECT_SCHEDULES[project.slug] || [];
  return {
    ...project,
    sessions,
    scheduleSummary: sessions.map((item) => `${item.label}：${item.date}`).join(" / ")
  };
}

